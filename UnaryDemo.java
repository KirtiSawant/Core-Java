
public class UnaryDemo {

	    public static void main(String[] args){
	        int i = 6;
	        i++;
	        // prints 7
	        System.out.println(i);
	        ++i;			   
	        // prints 8
	        System.out.println(i);
	        // prints 9
	        System.out.println(++i);
	        // prints 9
	        System.out.println(i++);
	        // prints 10
	        System.out.println(i);
	    }
	}

